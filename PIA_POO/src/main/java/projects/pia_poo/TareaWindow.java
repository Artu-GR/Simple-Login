    private void btInsertMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btInsertMouseClicked
        Tarea task = new Tarea();
        task.setNombre(txtName.getText());
        task.setDescripcion(txtDescription.getText());
        task.setFecha(txtDate.getText());
        task.setIdUser(userID);
        task.insertTarea();
        JOptionPane.showMessageDialog(rootPane, "Tarea agregada correctamente");
        cargarTareas();
        limpiarPantalla();
    }//GEN-LAST:event_btInsertMouseClicked

    private void btDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btDeleteMouseClicked
        Tarea task = new Tarea();
        task.setID(Integer.parseInt(txtID.getText()));
        task.delete();
        JOptionPane.showMessageDialog(rootPane, "Tarea eliminada correctamente");
        cargarTareas();
        limpiarPantalla();
    }//GEN-LAST:event_btDeleteMouseClicked

    private void btUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btUpdateMouseClicked
        Tarea task = new Tarea();
        task.setID(Integer.parseInt(txtID.getText()));
        task.setNombre(txtName.getText());
        task.setDescripcion(txtDescription.getText());
        task.setFecha(txtDate.getText());
        task.update();
        JOptionPane.showMessageDialog(rootPane, "Tarea actualizada correctamente");
        cargarTareas();
        limpiarPantalla();
    }//GEN-LAST:event_btUpdateMouseClicked

