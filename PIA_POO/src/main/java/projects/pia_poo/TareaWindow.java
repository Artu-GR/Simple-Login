    private void cargarTareas(){
        try {
            // Cargar tareas desde la base de datos
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/tareas_pia_poo?enabledTLSProtocols=TLSv1.2", "root", "ijklmnop582#");

            ResultSet rs = con.createStatement().executeQuery("SELECT * FROM tareas where idUser="+userID);
            
            // Crear modelo de tabla para mostrar las tareas
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID");
            model.addColumn("Nombre");
            model.addColumn("Descripci√≥n");
            model.addColumn("Fecha");
            model.addColumn("Responsable");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nombre"),
                    rs.getString("descripcion"),
                    rs.getDate("fecha"),
                    rs.getInt("idUser")
                });
            }

            // Establecer el modelo en la tabla
            grdPrincipal.setModel(model);
        } catch (ClassNotFoundException | SQLException ex) {
            System.err.println(ex.getMessage());
        }
    }

